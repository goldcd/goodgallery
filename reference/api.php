if ($action === 'get_related_tags') {
    $search = isset($_GET['q']) ? trim($_GET['q']) : '';
    
    // Base SQL
    $sql = "SELECT tags FROM anon_image_tags WHERE 1=1";
    
    // --- DUPLICATE SEARCH LOGIC (Boolean Support) ---
    // Matches logic in index.php (to be updated)
    // Terms: "word" (AND), "+word" (AND), "-word" (NOT)
    // Supports Quoted: +"multi word"
    if ($search !== '') {
        // Regex to find tokens: 
        // 1. Optional + or -
        // 2. Quote string OR non-space sequence
        preg_match_all('/([+-]?)(?:"([^"]*)"|([^\s"]+))/', $search, $matches, PREG_SET_ORDER);
        
        foreach ($matches as $m) {
            $prefix = $m[1];
            $term = !empty($m[2]) ? $m[2] : (isset($m[3]) ? $m[3] : '');
            
            $term = trim($term);
            if (!$term) continue;
            
            $not = ($prefix === '-');
            // Explicit + is treated as normal AND
            
            $escaped = mysqli_real_escape_string($connection, $term);
            // Universal Whole Word Regex
            $regex = "(^|[^a-zA-Z0-9])$escaped([^a-zA-Z0-9]|$)";
            
            if ($not) {
                $sql .= " AND tags NOT REGEXP '$regex'";
            } else {
                $sql .= " AND tags REGEXP '$regex'";
            }
        }
    }
    
    $tagsCount = [];
    $res = mysqli_query($connection, $sql);
    
    if ($res) {
        while ($row = mysqli_fetch_assoc($res)) {
            if (!$row['tags']) continue;
            $parts = explode(',', $row['tags']);
            foreach ($parts as $t) {
                $t = trim(strtolower($t));
                if ($t === "") continue;
                
                // We now include ALL tags so the frontend can mark them as active
                // if (stripos($search, $t) !== false) continue;
                
                if (!isset($tagsCount[$t])) $tagsCount[$t] = 0;
                $tagsCount[$t]++;
            }
        }
    }
    
    // Sort
    arsort($tagsCount);
    
    // Return Top 30
    $top = array_slice($tagsCount, 0, 30);
    echo json_encode($top);
    exit;
}

