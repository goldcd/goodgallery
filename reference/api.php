                if (@unlink($fullPath)) {
                    // Force Cache Rebuild since file is gone
                    @unlink(__DIR__ . '/cache/file_index.json');
                    // Count as processed since it's gone
                    $processed++; 
                }
            }
        }
    }

    echo json_encode(["status" => "ok", "processed" => $processed]);
    exit;
