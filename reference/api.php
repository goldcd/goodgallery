if ($action === 'get_untagged') {
    // 1. Get list of all files in directory (using cache if available for speed)
    // We can reuse the cache logic from index.php or just scan directory if cache is old.
    // For simplicity/robustness, let's look at the cache file generated by index.php
    $cacheFile = __DIR__ . '/cache/file_index.json';
    $allFiles = [];
    
    if (file_exists($cacheFile)) {
        $json = json_decode(file_get_contents($cacheFile), true);
        if (isset($json)) { // Check if json_decode succeeded
            foreach($json as $item) $allFiles[] = $item['name'];
        }
    } else {
        // Fallback: simple scan
        $scanned = scandir(__DIR__);
        foreach ($scanned as $f) {
            if ($f === '.' || $f === '..') continue;
            if (is_dir(__DIR__ . '/' . $f)) continue; // Skip subdirs
            
            $ext = strtolower(pathinfo($f, PATHINFO_EXTENSION));
            if (in_array($ext, ['jpg', 'jpeg', 'png', 'gif', 'webp'])) {
                $allFiles[] = $f;
            }
        }
    }

    // FILTER OUT BAD FILES (Emojis) - Apply to Cache AND Scan results
    $cleanFiles = [];
    foreach ($allFiles as $f) {
         if (preg_match('/[\x{10000}-\x{10FFFF}]/u', $f)) continue;
         $cleanFiles[] = $f;
    }
    $allFiles = $cleanFiles;
    
    // 2. Get list of files that ARE tagged
    $taggedFiles = [];
    $res = mysqli_query($connection, "SELECT filename FROM anon_image_tags");
    while ($row = mysqli_fetch_assoc($res)) {
        $taggedFiles[$row['filename']] = true;
    }
    
    // 3. Diff (Case Insensitive + Accent Insensitive to handle MySQL Collisions)
    $taggedNormalized = [];
    foreach ($taggedFiles as $tf => $val) {
        $taggedNormalized[normalize_filename($tf)] = true;
    }

    $untagged = [];
    $limit = isset($_GET['limit']) ? (int)$_GET['limit'] : 50; 
    foreach ($allFiles as $f) {
        // Compare normalized filename against normalized tagged list
        // If DB has 'vader' and disk has 'väder', strict compare fails, but normalized compare matches.
        if (!isset($taggedNormalized[normalize_filename($f)])) {
            $untagged[] = $f;
            if (count($untagged) >= $limit) break;
        }
    }
    
    echo json_encode(["status" => "ok", "files" => $untagged]);
    exit;
}

if ($action === 'save_tags') {
    // Expect JSON POST body
    $input = json_decode(file_get_contents('php://input'), true);
    
    if (!$input) {
        die(json_encode(["error" => "Invalid input"]));
    }

    // Normalize input to array of items
    $items = [];
    if (isset($input['filename'])) {
        // Single item format
        $items[] = $input;
    } else if (is_array($input)) {
        // Batch format (list of items)
        $items = $input;
    } else {
        die(json_encode(["error" => "Invalid input format"]));
    }
    
    $processed = 0;
    foreach ($items as $item) {
        if (!isset($item['filename']) || !isset($item['tags'])) continue;

        $filename = mysqli_real_escape_string($connection, $item['filename']);
        // Handle both array and string input for tags just in case
        $tagsStr = is_array($item['tags']) ? implode(',', $item['tags']) : $item['tags'];
        
        $tagsSqlValue = "NULL";
        if ($tagsStr !== "") {
             $escapedTags = mysqli_real_escape_string($connection, $tagsStr);
             $tagsSqlValue = "'$escapedTags'";
        }
        
        // UPSERT strategy: INSERT ... ON DUPLICATE KEY UPDATE
        // This handles case-insensitive collisions gracefully (e.g. 'File.jpg' updates 'file.jpg')
        // CRITICAL FIX: We also update 'filename' to match what's on disk.
        // If DB has 'väder.png' and disk has 'vader.png', MySQL collation might match them,
        // preventing a new row. We must force DB to become 'vader.png' so PHP sees them as identical next time.
        $sql = "INSERT INTO anon_image_tags (filename, tags) VALUES ('$filename', $tagsSqlValue) 
                ON DUPLICATE KEY UPDATE tags = VALUES(tags), filename = VALUES(filename)";
        if (mysqli_query($connection, $sql)) {
            $processed++;
        } else {
            // Database Error (Likely encoding issue with emojis in filename vs utf8mb3 DB)
            // User Instruction: "If an image is malformed, it should just be deleted."
