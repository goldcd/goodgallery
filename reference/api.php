if ($action === 'get_tags') {
    $cacheFile = __DIR__ . '/cache/tags_index.json';
    $cacheLife = 3600; // 1 hour

    // Serve cache if valid
    if (file_exists($cacheFile) && (time() - filemtime($cacheFile) < $cacheLife) && !isset($_GET['rebuild'])) {
        header('Content-Type: application/json');
        readfile($cacheFile);
        exit;
    }

    // Rebuild Cache
    $tagsCount = [];
    $res = mysqli_query($connection, "SELECT tags FROM anon_image_tags");
    
    while ($row = mysqli_fetch_assoc($res)) {
        if (!$row['tags']) continue;
        // Explode by comma, trim, lowercase
        $parts = explode(',', $row['tags']);
        foreach ($parts as $t) {
            $t = trim(strtolower($t));
            if ($t === "") continue;
            if (!isset($tagsCount[$t])) $tagsCount[$t] = 0;
            $tagsCount[$t]++;
        }
    }

    // Sort by frequency (Desc)
    arsort($tagsCount);
    
    // Filter out tags with count < 2 (Noise reduction)
    $filteredTags = [];
    foreach ($tagsCount as $tag => $count) {
        if ($count >= 2) {
            $filteredTags[] = $tag;
        }
    }
    
    // Save
    $json = json_encode(array_values($filteredTags));
    file_put_contents($cacheFile, $json);

    echo $json;
    exit;
}

