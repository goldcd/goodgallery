function normalize_filename($str) {
    // 1. Lowercase
    $str = mb_strtolower($str, 'UTF-8');
    // 2. Transliterate Accents (Simple manual map for common European chars to match MySQL utf8_general_ci behavior)
    $str = str_replace(
        ['à','á','â','ã','ä','å', 'ç', 'è','é','ê','ë', 'ì','í','î','ï', 'ñ', 'ò','ó','ô','õ','ö', 'ù','ú','û','ü', 'ý','ÿ'],
        ['a','a','a','a','a','a', 'c', 'e','e','e','e', 'i','i','i','i', 'n', 'o','o','o','o','o', 'u','u','u','u', 'y','y'],
        $str
    );
    return $str;
}

